package com.technoelevate.hasarelation;

public class Heart 
{
	int heartBeat;
	float weight;
	public Heart(int heartBeat, float weight) {
		super();
		this.heartBeat = heartBeat;
		this.weight = weight;
	}
	public int getHeartBeat() {
		return heartBeat;
	}
	public float getWeight() {
		return weight;
	}
	
	
}
