package com.technoelevate.hasarelation;

public class Bike 
{
	String brand;
	float mileage;
	public Bike(String brand, float mileage) {
		super();
		this.brand = brand;
		this.mileage = mileage;
	}
	public String getBrand() {
		return brand;
	}
	public float getMileage() {
		return mileage;
	}
	
	
}
