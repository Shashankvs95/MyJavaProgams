package com.technoelevate.hasarelation;

public class OperatingSystem {
	String name;
	float version;
	public OperatingSystem(String name, float version) {
		super();
		this.name = name;
		this.version = version;
	}
	public String getName() {
		return name;
	}
	
	public float getVersion() {
		return version;
	}
	
	

}
